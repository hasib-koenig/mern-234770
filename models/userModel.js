import mongoose from "mongoose";

//schema -> proper shape for the data
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//model -> collection of the data
export const userModel = mongoose.model("User", userSchema);
